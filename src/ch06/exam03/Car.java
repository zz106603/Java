package ch06.exam03;

public class Car {

	String company;
	String model;
	int price;
	boolean old;
	int speed;
	
	
	public Car(String company, String model, int price, boolean old, int speed) {
		this.company = company;
		this.model = model;
		this.price = price;
		this.old = old;
		this.speed = speed;
	}
	
	
}
